  public void draw() {
    int termninalWidth = 100;
    int length = 10;
    int size = 25;
    int max = (int) (size * Math.sqrt(length - 1));
    for (int i = 1; i < length; i++) {

      drawFlatLine((int) (size * Math.sqrt(i)), termninalWidth, "▓");
    }
    System.out.println();
    for (int i = 1; i < 15; i++) {
      drawFlatLine((int) 60, termninalWidth, "▓");
    }
    System.out.println();
    for (int i = 1; i < 6; i++) {
      drawFlatLine((int) max, termninalWidth, "▓");
    }
    for (int i = 1; i < 3; i++) {
      drawFlatLine((int) max - 5, termninalWidth, "▓");
    }

    System.out.println();
  }

  public void drawFlatLine(int length, int termninalWidth, String c) {
    Random r = new Random();
    int random = r.nextInt(10);
    if (random == 0) {
      c = " ";
    }
    System.out.print(" ".repeat((termninalWidth - length) / 2));
    System.out.print(c.repeat(length) + "\n");
  }

